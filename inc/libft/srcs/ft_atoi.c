#include <libft.h>

int	ft_atoi(const char *str)
{
	int		n;
	int		x;

	n = 0;
	x = 1;
	while (*str <= 32)
		str++;
	if (*str == '-' || *str == '+')
		if (*(str++) == '-')
			x = -1;
	while (*str >= 48 && *str <= 57)
		n = (n * 10) + (*(str++) - '0');
	return (x * n);
}
